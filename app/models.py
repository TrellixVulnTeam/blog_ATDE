class Permission:
    FOLLOW = 0x01            #关注用户
    COMMENT = 0x02           #在他人的文章中发表评论
    WRITE_ARTICLES = 0x04    #写文章
    MODERATE_COMMENTS = 0x08 #管理他人发表的评论
    ADMINISTER = 0x80        #管理员权限